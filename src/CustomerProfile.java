    public void details(){
       String sql="select * from Account where Phone_no=?";
        try {
            pst=conn.prepareStatement(sql);
            pst.setString(1, user);
            rs=pst.executeQuery();
            if(rs.next()) {
                String add=rs.getString("Phone_no");
                phone.setText(add);
                String add1=rs.getString("Surname");
                surname.setText(add1);
                String add2=rs.getString("Firstname");
                firstname.setText(add2);
                String add3=rs.getString("Occupation");
                occupation.setText(add3);
                String add4=rs.getString("Address");
                address.setText(add4);
                rs.close();
                pst.close();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

