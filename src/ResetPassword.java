         public void updateQuery(String table_name, String mobile, String password) {
        try {
           String sql="UPDATE '"+table_name+"' SET Password=? WHERE Phone_no=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1, password);
            pst.setString(2, mobile);
            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null, "Your password has been "
                    + "successfully changed\nYou can now login with new password");
        }
       
        catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }
