   public void update(){
       forgot();
     String  password1 = txtpassword.getText().trim();
     String  password2 = txtconfirm.getText().trim();
     if(! password1.equals(password2)){
         JOptionPane.showMessageDialog(null, "Password and confirm password "
                 + "field does not match \n Re-confirm");
     }
     else if(password1.length() < 6){
         JOptionPane.showMessageDialog(null, "Password must not less than six characters long");
     }
     else if(phone_no.equals(cstPhone) && password1.length() >= 6){
         updateQuery("Account", cstPhone, password1);
         setVisible(false);
        Login ob=new Login();
        ob.setVisible(true);
     }
     else if(phone_no.equals(stfPhone) && password1.length() >= 6){
         updateQuery("Staff", stfPhone, password1);
         setVisible(false);
        Login ob=new Login();
        ob.setVisible(true);
     }
     else if(phone_no.equals(stdPhone) && password1.length() >= 6){
         updateQuery("Student", stdPhone, password1);
         setVisible(false);
        Login ob=new Login();
        ob.setVisible(true);
     }
     else {
         JOptionPane.showMessageDialog(null, "Connection Failed!");
     }
   }     
         

